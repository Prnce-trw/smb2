        document.querySelector('.custom-file-input').addEventListener('change', function(e) {
            var fileName = document.getElementById("myInput").files[0].name;
            var nextSibling = e.target.nextElementSibling
            nextSibling.innerText = fileName
        })

        $('.close').click(function() {
            $('#newsHeading').parent().slideUp();
        })

        //Reduce quantity by 1 if clicked
        $(document).on("click", ".product-quantity-subtract", function(e) {
            var value = $("#product-quantity-input-checkout").val();
            //console.log(value);
            var newValue = parseInt(value) - 1;
            if (newValue < 0) newValue = 0;
            $("#product-quantity-input-checkout").val(newValue);
            CalcPrice(newValue);
        });

        //Increase quantity by 1 if clicked
        $(document).on("click", ".product-quantity-add", function(e) {
            var value = $("#product-quantity-input-checkout").val();
            //console.log(value);
            var newValue = parseInt(value) + 1;
            $("#product-quantity-input-checkout").val(newValue);
            CalcPrice(newValue);
        });

        $(document).on("blur", "#product-quantity-input-checkout", function(e) {
            var value = $("#product-quantity-input-checkout").val();
            //console.log(value);
            CalcPrice(value);
        });


        function AddToCart(e) {
            e.preventDefault();
            var qty = $("#product-quantity-input-checkout").val();
            if (qty === '0') {
                return;
            }
            var toast = '<div class="toast toast-success">Added ' + qty + ' to cart.</div>';
            $("body").append(toast);
            setTimeout(function() {
                $(".toast").addClass("toast-transition");
            }, 100);
            setTimeout(function() {
                $(".toast").remove();
            }, 3500);
        }
    </script>
